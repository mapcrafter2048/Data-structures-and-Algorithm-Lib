int maxDistance(vector<int> positions, int m) {
    int n = positions.size();
    sort(positions.begin(), positions.end());
    int minForce = 1;
    int maxForce = positions[n - 1] - positions[0];
    int result = 0;

    while (minForce <= maxForce) {
        int midForce = minForce + (maxForce - minForce) / 2;

        if (possible(midForce, positions, m)) {
            result = midForce;
            minForce = midForce + 1;
        } else {
            maxForce = midForce - 1;
        }
    }

    return result;
}

bool possible(int midForce, vector<int> positions, int m) {
    int count = 1;
    int lastPosition = positions[0];

    for (int i = 1; i < positions.size(); i++) {
        if (positions[i] - lastPosition >= midForce) {
            count++;
            lastPosition = positions[i];
        }
    }

    return count >= m;
}

