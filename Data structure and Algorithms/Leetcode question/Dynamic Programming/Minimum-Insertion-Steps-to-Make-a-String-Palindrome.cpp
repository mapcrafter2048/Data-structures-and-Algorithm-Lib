long long int binpow(long long int b, long long int p, long long int mod) {
    long long int ans = 1;
    b %= mod;
    for (; p; p >>= 1) {
        if (p & 1)
            ans = ans * b % mod;
        b = b * b % mod;
    }
    return ans;
}

using namespace std;

int helper(string sf, string sr, int i, int j, vector<vector<int>> dp) {
    if (i == 0 || j == 0) {
        return 0;
    }

    if (dp[i][j] != -1) {
        return dp[i][j];
    }

    if (sf[i - 1] == sr[j - 1]) {
        dp[i][j] = 1 + helper(sf, sr, i - 1, j - 1, dp);
    } else {
        dp[i][j] =
            max(helper(sf, sr, i - 1, j, dp), helper(sf, sr, i, j - 1, dp));
    }

    return dp[i][j];
}

int minInsertions2(string s) {
    string sr = s;
    reverse(sr.begin(), sr.end());
    int n = s.length();
    vector<vector<int>> dp(n + 1, vector<int>(n + 1, -1));
    return n - helper(s, sr, n, n, dp);
}

int minInsertions(string s) {
    int n = s.length();

    string ans = s;

    reverse(ans.begin(), ans.end());
    vector<vector<int>> dp(n + 1, vector<int>(n + 1, 0));
    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= n; j++) {
            if (s[i - 1] == ans[j - 1])
                dp[i][j] = 1 + dp[i - 1][j - 1];
            else
                dp[i][j] = max(dp[i - 1][j], dp[i][j - 1]);
        }
    }
    int len = dp[n][n];

    return n - len;
}

