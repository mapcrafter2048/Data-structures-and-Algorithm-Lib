vector<int> kahnAlgortihms(vector<vector<int>> &graph, vector<int> &inDegree)
{
    int numNodes = graph.size();
    vector<int> result;
    queue<int> q;

    for (int i = 0; i < numNodes; i++)
    {
        if (inDegree[i] == 0)
        {
            q.push(i);
        }
    }

    while (!q.empty())
    {
        int node = q.front();
        q.pop();
        result.push_back(node);

        for (int neighbor : graph[node])
        {
            inDegree[neighbor]--;
            if (inDegree[neighbor] == 0)
            {
                q.push(neighbor);
            }
        }
    }
    if (result.size() != numNodes)
    {
        cout << " The graph has a cycle " << endl;
        return {};
    }
    return result;
}

